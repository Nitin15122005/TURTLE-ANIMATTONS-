t.pencolor('navy')
for i in range(24):
    t.fillcolor('navy')
    t.begin_fill()
    t.circle(4.6)
    t.left(15)
    t.width(12)
    t.forward(15)
    t.width(0)
    t.end_fill()
t.penup()
t.setpos(0,0)
t.backward(8)
t.pendown()
t.right(90)
t.forward(2)
for i in range(24):
    t.width(7)
    t.forward(10)
    t.width(2)
    t.forward(50)
    t.backward(60)
    t.left(15)
t.penup()
t.speed(6)
t.forward(300)
